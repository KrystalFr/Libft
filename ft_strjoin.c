#include "libft.h"
#include <stdlib.h>

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	len1;
	size_t	len2;
	char	*result;

	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	result = malloc((len1 + len2 + 1) * sizeof(char));
	if (result)
	{
		ft_strlcpy(result, s1, len1 + 1);
		ft_strlcat(result, s2, len1 + len2 + 1);
	}
	return (result);
}
