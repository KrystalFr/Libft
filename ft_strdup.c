#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*tab;

	i = 0;
	tab = malloc((ft_strlen(src) + 1) * sizeof(char));
	if (!tab)
		return (NULL);
	while (src[i])
	{
		tab[i] = src[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
